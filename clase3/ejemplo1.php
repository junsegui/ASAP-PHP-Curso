<?php 
//Iteracion
//////////////////////////////////////////////////////////////////////////
//While
$cant = 1;
$c = 1;

while ($cant<=10) {
    echo $cant . " *  2 = " . ($cant * 2) ."<br>";
    $cant++;
};
//Do While
do {
    echo $c . " *  3 = " . ($c * 2) . "<br>";
    $c++;
} while ($c <= 10);
//For
for ($i=1;$i<=10;$i++) {
    echo $i . " *  5 = " . ($i * 5) ."<br>";

}
;

?>